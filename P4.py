#This program will convert degrees Fahrenheit to degrees Celsius

# Prompt the user to enter the miles driven and gallons consumed
miles_driven = float(input("Enter the number of miles driven: "))
gallons_consumed = float(input("Enter the number of gallons consumed: "))

# Compute the MPG (Miles Per Gallon)
mpg = miles_driven / gallons_consumed

# Print the computed MPG
print("The MPG for the car is:", mpg)

